func RegisterRename(b *bot.Bot) {
	b.OnCommand("随机取名").Handle(func(ctx *bot.CommandContext) error {
		name := ""
		if len(ctx.Args) > 0 {
			name = ctx.Args[0]
		}
		if name == "" {
			name = el1[rand.Intn(len(el1))] + el2[rand.Intn(len(el2))] + el3[rand.Intn(len(el3))]
		}
		if err := ctx.SetGroupCard(ctx.GroupID(), ctx.UserID(), name); err != nil {
			return ctx.Reply("改名失败，可能缺少管理员权限")
		}
		return ctx.Reply("已将你的群名片改为: " + name)
	})
}
